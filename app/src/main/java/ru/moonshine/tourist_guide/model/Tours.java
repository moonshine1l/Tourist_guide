package ru.moonshine.tourist_guide.model;

public class Tours {
    int id;
    String title;
    String image;
    String price;

    public Tours(int id, String title, String image, String price) {
        this.id = id;
        this.title = title;
        this.image = image;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
